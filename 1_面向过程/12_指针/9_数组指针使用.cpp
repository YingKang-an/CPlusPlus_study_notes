#include <iostream>


int main (void)
{
    int a[5] = { 1, 2, 3, 4, 5 };
    int(*p)[5] = &a;

    // p 是变量，int(*)[5] 是类型
    // p == &a;
    // *p == *&a == a
    // *p 就跟 a 一样使用了 eg:a[0]

    int i;
    for (i = 0; i < 5; i++)
    {
        std::cout << (*p)[i] << std::endl;
    }

    /*************************************************** 
        // 读
        a[0] == (*p)[0];
        a[1] == (*p)[1];
        // 写
        (*p)[0] = 2;
        // 取地址
        &a[0] == &(*p)[0];
    ***************************************************/

//================================================================================    
    
    //二维数组类型指针
    int b[2][3] = { {3, 6, 2}, {1, 9, 7} };
    int(*p0)[2][3] = &b; // b是整个二维数组的地址
    //int(*)[2][3]是二维数组类型指针
    (*p0)[1][1] = 5; // b[1][1] = 5;

    return 0;
}





