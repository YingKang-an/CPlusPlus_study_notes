/*
    类：

    什么是类？

    从编码角度
        类的语法是 class，就是一种数据类型，跟 struct,int,double
    一样，然后用他定义变量，用 class 这种类型定义的变量叫对象，为什么呢？因
    为 class 这种类型的组成极其丰富，使得变量不仅仅是装数据的功能，还自带处
    理数据的功能，比 int a 这种变量强大一万倍，所以给他叫对象，比如小华，就
    是人类的一个个体，也叫对象。
        class 也是 struct 功能的拓展延伸，struct 用来装纯数据，class 里可以
    装函数访问权限以及各种新功能。

    抽象概念层面
        具有相同属性与功能的对象的合集，叫类。比如人类，昆虫类，汽车类，飞
    机类，都是各自的一个群体，叫类，单独拿出来一个具体的个体，叫对象
    概念性的文字，我们不用过分深究，把 class 相关的语法，用的非常熟练，
    就越来越能体会面向对象的设计思想了，不是一个月两个月能融会贯通的。

*/
