#include<iostream>

/*****************************************
 * 指针数组：元素为指针的数组
******************************************/

int main()
{
    int a[10];
    // 每个元素都是int类型数据
    int* b[10]; //每个元素都是int*类型数据

    int d = 1, e = 2, f = 3, g = 4, h = 5;
    int* c[5] ={ &d, &e, &f, &g, &h };

    // c[1]就是&e
    // *c[1] == *&e == e

    std::cout << *c[1] << std::endl;
    *c[1] = 23;
    std::cout << *c[1] << std::endl;
    
    return 0;
}